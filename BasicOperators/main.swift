///## **[Section 2: Arithmetic Operators**]

let sum = 3 + 2
let difference = 5 - 3
let product = 4 * 2
let quotient = 8 / 2
let remainder = 7 % 3

print("Sum: \(sum), Difference: \(difference), Product: \(product), Quotient: \(quotient), Remainder: \(remainder)")

///## **[Section 3: Comparison Operators**]

let a = 5
let b = 3

print(a == b) // false, because 5 is not equal to 3
print(a != b) // true, because 5 is not equal to 3
print(a > b)  // true, because 5 is greater than 3
print(a <= b) // false, because 5 is not less than or equal to 3

///## **[Section 4: Logical Operators**]

let isSwiftFun = true
let isDifficult = false

print(isSwiftFun && !isDifficult) // true
print(isSwiftFun || isDifficult) // true
print(!isSwiftFun) // false

